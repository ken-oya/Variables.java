
public class basicSyntax {

	public static void main(String[] args) {
		int num1 = 9;
		double num2 = 0.19;
		
		String firstNote = "私の好きな";
		String secondNote = "整数";
		String thirdNote = "少数";
		String fnNote = "です。";
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(firstNote + secondNote + "は" + num1 + fnNote);
		System.out.println(firstNote + thirdNote + "は" + num2 + fnNote);

	}

}
